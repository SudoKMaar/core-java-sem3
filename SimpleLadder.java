class SimpleLadder {
    public static void main(String[] args) {
        String city = "Noida";
        if (city == "Meerut") {
            System.out.println("city is meerut");
        } else if (city == "Faridabad") {
            System.out.println("city is faridabad");
        } else if (city == "Delhi") {
            System.out.println("city is delhi");
        } else {
            System.out.println(city);
        }
    }
}